#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import { existsSync, readFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const changelogPath = path.join(repoRoot, "CHANGELOG.md");
const releaseConfigPath = path.join(repoRoot, ".github", "release.yml");
const templatePath = "docs/changelog-template.md";

const allowedSectionHeadings = new Set([
  "Added",
  "Changed",
  "Fixed",
  "Removed",
  "Security",
  "Documentation",
]);

const monitoredPrefixes = [
  "src/",
  "docs/",
  "scripts/",
  "tests/fixtures/",
  ".github/workflows/",
];
const monitoredFiles = new Set([
  "README.md",
  "package.json",
  "package-lock.json",
  "tsconfig.json",
  "AGENTS.md",
  ".github/release.yml",
]);
const ignoredChangedFiles = new Set([
  "CHANGELOG.md",
]);

const join = (...parts) => parts.join("");
const sourceAnalysisTerm = `${join("rev", "erse")}[- ]${join("engine", "er")}(?:ed|ing)?`;
const bundleAnalysisTerm = `${join("deco", "mpil")}(?:e|ed|ation|ing)`;
const artifactTerm = join("extra", "cted");

const publicUnsafePatterns = [
  new RegExp(sourceAnalysisTerm, "i"),
  new RegExp(bundleAnalysisTerm, "i"),
  new RegExp(`${artifactTerm}\\s+(?:bundle|source|artifact)`, "i"),
  new RegExp(`runtime\\s+${join("tra", "ce")}`, "i"),
  new RegExp(`prompt\\s+${join("extra", "ction")}`, "i"),
  new RegExp(`private\\s+(?:dump|evidence|${join("ref", "erence")})`, "i"),
  new RegExp(`${join("cop", "ied")}\\s+from\\s+the\\s+original\\s+system\\s+prompt`, "i"),
  new RegExp(`matches\\s+${join("up", "stream")}\\s+${join("inter", "nals")}`, "i"),
  new RegExp(`ported\\s+from\\s+${artifactTerm}\\s+source`, "i"),
  new RegExp(`${join("veri", "fied")}\\s+against\\s+a\\s+private\\s+dump`, "i"),
  new RegExp(`${join("par", "ity")}\\s+with\\s+the\\s+internal\\s+tool\\s+list`, "i"),
];

function git(args) {
  try {
    return execFileSync("git", ["-C", repoRoot, ...args], { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"] }).trim();
  } catch {
    return "";
  }
}

function addLines(set, text) {
  for (const line of text.split("\n")) {
    const trimmed = line.trim();
    if (trimmed) set.add(trimmed.replaceAll("\\", "/"));
  }
}

function getChangedFiles() {
  const files = new Set();
  const baseRef = process.env.PI_MMR_CHANGELOG_BASE_REF || "origin/main";
  const mergeBase = git(["merge-base", baseRef, "HEAD"]);
  if (mergeBase) {
    addLines(files, git(["diff", "--name-only", "--diff-filter=ACMR", `${mergeBase}...HEAD`]));
  }
  addLines(files, git(["diff", "--name-only", "--diff-filter=ACMR", "--cached"]));
  addLines(files, git(["diff", "--name-only", "--diff-filter=ACMR"]));
  addLines(files, git(["ls-files", "--others", "--exclude-standard"]));
  return [...files];
}

function isMonitored(file) {
  if (ignoredChangedFiles.has(file)) return false;
  if (monitoredFiles.has(file)) return true;
  return monitoredPrefixes.some((prefix) => file.startsWith(prefix));
}

function lineNumberForOffset(text, offset) {
  return text.slice(0, offset).split("\n").length;
}

function validateReleaseNotesConfig(configText) {
  const errors = [];
  if (!/^changelog:\s*$/m.test(configText)) {
    errors.push(".github/release.yml must configure GitHub autogenerated release notes under a top-level 'changelog:' key.");
  }
  for (const requiredTitle of allowedSectionHeadings) {
    const titlePattern = new RegExp(`^\\s*-\\s+title:\\s+["']?${requiredTitle}["']?\\s*$`, "m");
    if (!titlePattern.test(configText)) {
      errors.push(`.github/release.yml must include a release-note category titled '${requiredTitle}'.`);
    }
  }
  for (const match of configText.matchAll(/^\s*-\s+title:\s+["']?(.+?)["']?\s*$/gm)) {
    const title = match[1];
    if (!allowedSectionHeadings.has(title)) {
      errors.push(`.github/release.yml category '${title}' must match a CHANGELOG.md heading.`);
    }
  }
  if (!/skip-changelog/.test(configText)) {
    errors.push(".github/release.yml should exclude the skip-changelog label for deliberately non-user-visible PRs.");
  }
  return errors;
}

function validateChangelogShape(markdown) {
  const errors = [];
  if (!markdown.startsWith("# Changelog\n")) {
    errors.push("CHANGELOG.md must start with '# Changelog'.");
  }
  if (!/^All notable changes to `pi-mmr`/m.test(markdown)) {
    errors.push("CHANGELOG.md must keep the pi-mmr notable-changes introduction.");
  }
  if (!/^## Unreleased\s*$/m.test(markdown)) {
    errors.push("CHANGELOG.md must contain a top-level '## Unreleased' section.");
  }

  const headingMatches = [...markdown.matchAll(/^###\s+(.+?)\s*$/gm)];
  for (const match of headingMatches) {
    const heading = match[1];
    if (!allowedSectionHeadings.has(heading)) {
      errors.push(`Unsupported changelog heading '${heading}' at line ${lineNumberForOffset(markdown, match.index ?? 0)}. Allowed headings: ${[...allowedSectionHeadings].join(", ")}.`);
    }
  }

  const lines = markdown.split("\n");
  for (let index = 0; index < lines.length; index += 1) {
    const heading = lines[index].match(/^###\s+(.+?)\s*$/)?.[1];
    if (!heading) continue;
    let hasBullet = false;
    for (let cursor = index + 1; cursor < lines.length; cursor += 1) {
      if (lines[cursor].startsWith("## ") || lines[cursor].startsWith("### ")) break;
      if (lines[cursor].startsWith("- ")) {
        hasBullet = true;
        break;
      }
    }
    if (!hasBullet) {
      errors.push(`Changelog heading '${heading}' at line ${index + 1} must contain at least one top-level bullet.`);
    }
  }

  for (const pattern of publicUnsafePatterns) {
    const match = markdown.match(pattern);
    if (match?.index !== undefined) {
      errors.push(`CHANGELOG.md contains public-unsafe wording near line ${lineNumberForOffset(markdown, match.index)}: ${JSON.stringify(match[0])}.`);
    }
  }

  return errors;
}

/**
 * Pure predicate: do the given changed files require a CHANGELOG.md entry?
 * True when at least one monitored file changed and CHANGELOG.md itself was
 * not among the changes. Shared by the local notice path and the CI
 * enforcement script (scripts/require-changelog-entry.mjs).
 *
 * @param {string[]} changedFiles
 * @returns {boolean}
 */
export function requiresChangelogEntry(changedFiles) {
  const monitored = changedFiles.filter(isMonitored);
  const changelogChanged = changedFiles.includes("CHANGELOG.md");
  return monitored.length > 0 && !changelogChanged;
}

/**
 * Decide whether a missing CHANGELOG.md entry should block.
 *
 * Default policy: do NOT block. A missing entry is a non-fatal notice, because
 * the canonical path is the PR-body marker block (see docs/changelog-template.md
 * "Automated PR-body sync"), which `scripts/sync-changelog-from-pr.mjs` appends
 * at PR time via the `changelog-sync` workflow. This removes the pre-test manual
 * CHANGELOG edit. The public-safe scan and shape validation still hard-fail.
 *
 * Opt-in strict mode: set `PI_MMR_CHANGELOG_REQUIRE_ENTRY=1` to turn a missing
 * entry back into a blocking error. `PI_MMR_CHANGELOG_NOT_NEEDED=1` silences the
 * notice entirely for a deliberately non-user-visible change.
 *
 * @returns {{ error?: string, notice?: string }}
 */
export function evaluateChangelogEntryPolicy({ monitoredChanges, changelogChanged, env = process.env }) {
  if (changelogChanged || monitoredChanges.length === 0 || env.PI_MMR_CHANGELOG_NOT_NEEDED === "1") {
    return {};
  }
  const shown = monitoredChanges.slice(0, 12).join(", ") + (monitoredChanges.length > 12 ? ", ..." : "");
  const message = [
    "CHANGELOG.md was not updated for monitored pi-mmr changes.",
    `Changed monitored files: ${shown}`,
    `Add a public-safe bullet via the PR-body marker block (${templatePath}, "Automated PR-body sync") so changelog-sync appends it at PR time, edit ## Unreleased directly, or set PI_MMR_CHANGELOG_NOT_NEEDED=1 for a deliberately non-user-visible change.`,
  ].join("\n");
  if (env.PI_MMR_CHANGELOG_REQUIRE_ENTRY === "1") {
    return { error: message };
  }
  return { notice: message };
}

function main() {
  const errors = [];
  if (!existsSync(changelogPath)) {
    errors.push("CHANGELOG.md is required.");
  } else {
    errors.push(...validateChangelogShape(readFileSync(changelogPath, "utf8")));
  }

  if (!existsSync(releaseConfigPath)) {
    errors.push(".github/release.yml is required so GitHub autogenerated release notes use the same categories as CHANGELOG.md.");
  } else {
    errors.push(...validateReleaseNotesConfig(readFileSync(releaseConfigPath, "utf8")));
  }

  const changedFiles = getChangedFiles();
  const monitoredChanges = changedFiles.filter(isMonitored);
  const changelogChanged = changedFiles.includes("CHANGELOG.md");
  const { error, notice } = evaluateChangelogEntryPolicy({ monitoredChanges, changelogChanged });
  if (error) errors.push(error);
  if (notice) console.error(`note: ${notice}`);

  if (errors.length > 0) {
    console.error(errors.map((error) => `- ${error}`).join("\n"));
    process.exitCode = 1;
  }
}

const invokedAsCli = (() => {
  try {
    return Boolean(process.argv[1]) && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);
  } catch {
    return false;
  }
})();

if (invokedAsCli) {
  main();
}
